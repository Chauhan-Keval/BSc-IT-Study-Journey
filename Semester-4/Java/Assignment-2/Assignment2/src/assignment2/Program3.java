    private void btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActionPerformed
        int d = Integer.parseInt(day.getSelectedItem().toString());
        int m = Integer.parseInt(month.getSelectedItem().toString());
        int y = Integer.parseInt(year.getText());
        
        LocalDateTime date = LocalDateTime.of(y,m,d,0,0);
        
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("EEE MMM dd HH:mm:ss zzz yyyy");
        String formattedDate = date.atZone(ZoneId.of("Asia/Kolkata")).format(formatter);
        
        result.setText(formattedDate);
    }//GEN-LAST:event_btnActionPerformed

