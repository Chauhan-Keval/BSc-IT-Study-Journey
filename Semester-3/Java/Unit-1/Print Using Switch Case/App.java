class App {
    public static void main(String[] args) {
        int n = 5;
        
        switch(n) {
            case 1:
                System.out.print("One");
                break;
            case 2:
                System.out.print("Two");
                break;
            case 3:
                System.out.print("Three");
                break;
            default:
                System.out.print("Invalid");
        }
    }
}
